from collections import Counter
from pathlib import Path

from openpyxl import load_workbook

ROOT = Path(__file__).resolve().parent
DATASET = ROOT / "customer_support_response_evaluation_dataset.xlsx"
OUTPUT = ROOT / "analysis_summary.md"

wb = load_workbook(DATASET, data_only=True)
ws = wb["Evaluation Data"]
iaa = wb["IAA Sample (n=20)"]

rows = list(ws.iter_rows(values_only=True))
headers = rows[0]
records = [dict(zip(headers, row)) for row in rows[1:] if any(row)]

preferred = Counter(r["Preferred Response"] for r in records)
hallucination = Counter(r["Hallucination Present"] for r in records)
safety = Counter(r["Safety Flag"] for r in records)
category = Counter(r["Category"] for r in records)

iaa_rows = [row for row in iaa.iter_rows(values_only=True) if row and row[0] and str(row[0]).startswith("T") and row[3] in {"Y", "N"}]
agree = sum(1 for row in iaa_rows if row[3] == "Y")

summary = f"""# Analysis Summary

Generated by analyze_dataset.py.

## Dataset Snapshot

- Total evaluation rows: {len(records)}
- IAA sample rows used: {len(iaa_rows)}
- Inter-annotator agreement: {agree}/{len(iaa_rows)} ({agree / len(iaa_rows) * 100:.1f}%)

## Preferred Response Counts

- A: {preferred["A"]}
- B: {preferred["B"]}

## Hallucination Flags

- N: {hallucination["N"]}
- Y: {hallucination["Y"]}

## Safety Flags

- N: {safety["N"]}

## Category Breakdown
"""

for key, value in sorted(category.items()):
    summary += f"\n- {key}: {value}"

summary += """

## Notes

- Preferred responses split evenly between A and B in this dataset.
- Hallucination labels appear in 25 of 100 rows.
- The IAA worksheet includes footer content, so only valid ticket rows are counted.
"""

OUTPUT.write_text(summary + "\n", encoding="utf-8")
print(summary)
